const SOCIAL_ICON_BY_LABEL: Record<string, (props: IconProps) => ReactNode> = {
  instagram: InstagramIcon,
  facebook: FacebookIcon,
  linkedin: LinkedInIcon,
  pinterest: PinterestIcon,
}

export function hasSocialBrandIcon(label: string): boolean {
  return label.trim().toLowerCase() in SOCIAL_ICON_BY_LABEL
}

export function SocialBrandIcon({ label, className }: { label: string; className?: string }) {
  const Icon = SOCIAL_ICON_BY_LABEL[label.trim().toLowerCase()]
  if (!Icon) return null
  return <Icon className={className} />
}
